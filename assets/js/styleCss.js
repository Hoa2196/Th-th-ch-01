class StyleCss {

    background;
    borderRadius;
    width;
    padding;
    position;
    margin;

    content;
    height;
    top;
    zIndex;
    transform;
    
    constructor() {}

    getContent(){
        return this.content;
    }

    setContent(content) {
        this.content = content;
    }

    getHeight() {
        return this.height;
    }

    setHeight(height) {
        this.height = height;
    }

    getTop() {
        return this.top;
    }

    setTop(top) {
        this.top = top;
    }
    getZIndex() {
        return this.zIndex;
    }

    setZIndex(zIndex) {
        this.zIndex = zIndex;
    }

    getTransform() {
        return this.transform;
    }

    setTransform() {
        this.transform = this.transform;
    }

    getBackground() {
        return this.background;
    }

    getBorderRadius() {
        return this.borderRadius;
    }

    getWidth() {
        return this.width;
    }

    getPadding() {
        return this.padding;
    }

    getPosition() {
        return this.position;
    }

    getMargin() {
        return this.margin;
    }

    setBackground(background) {
        this.background = background;
    }

    setBorderRadius(borderRadius) {
        this.borderRadius = borderRadius;
    }

    setWidth(width) {
        this.width = width;
    }

    setPadding(padding) {
        this.padding = padding;
    }

    setPosition(position) {
        this.position = position;
    }

    setMargin(margin) {
        this.margin = margin;
    }
}